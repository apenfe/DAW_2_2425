var num1 = prompt("Introduce un numero:");
var num2 = prompt("Introduce otro numero:");

if (num1 > num2) {
    alert("El numero " + num1 + " es mayor que " + num2);
} else if (num1 < num2) {
    alert("El numero " + num1 + " es menor que " + num2);
} else {
    alert("Los numeros son iguales");
}

